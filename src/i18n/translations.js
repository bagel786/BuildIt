// Maps any language value to 'en' or 'es' for UI chrome lookups
export const uiLang = (lang) => {
  if (!lang) return 'en';
  if (lang === 'es') return 'es';
  const l = lang.toLowerCase();
  if (l.includes('spanish') || l.includes('español') || l.includes('espanol')) return 'es';
  return 'en';
};

